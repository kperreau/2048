#include "rush.h"

int			ft_cmp_tab(t_win *win)
{
	int		x;
	int		y;

	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			if (win->cpy[y][x] != win->tab[y][x])
				return (1);
			++x;
		}
		++y;
	}
	return (0);
}
