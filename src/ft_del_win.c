#include "rush.h"

void		ft_del_win(t_win *win)
{
	int		y;
	int		x;

	y = 0;
	while (y < 4)
	{
		x = 0;
		while (x < 4)
		{
			delwin(win->win[y][x]);
			++x;
		}
		++y;
	}
}
